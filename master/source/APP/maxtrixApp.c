uint8_t maxtrixAppSetImage(uint8_t *pImage)
{
	uint32_t i = 0;
	volatile uint32_t color = 0;
	if(pImage == NULL)
	{
		return 0;
	}
	showMode = showImage;
	for(i = 0; i < MATRIX_SIZE; i++)
	{

		color = (pImage[i * 3]) | (pImage[i * 3 + 1] << 8) | ((pImage[i * 3 + 2] << 16));
		GRBSetCell(i, color);
		color = 0;
	}
	return 1;
}

